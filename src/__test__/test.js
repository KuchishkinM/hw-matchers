import sorter from '../app';

test('sorter test', () => {
  const testMass = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];

  const resultMass = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];

  const result = sorter(testMass);
  expect(result).toEqual(resultMass);
});
